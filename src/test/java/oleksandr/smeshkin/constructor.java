package oleksandr.smeshkin;

public class constructor {

    String nnn ;

    public constructor(String nnn, float jj, double kk) {
        this.nnn = nnn;
        this.jj = jj;
        this.kk = kk;
    }

    float jj;

    public String getNnn() {
        return nnn;
    }

    public void setNnn(String nnn) {
        this.nnn = nnn;
    }

    public float getJj() {
        return jj;
    }

    public void setJj(float jj) {
        this.jj = jj;
    }

    public double getKk() {
        return kk;
    }

    public void setKk(double kk) {
        this.kk = kk;
    }

    double kk;


}
